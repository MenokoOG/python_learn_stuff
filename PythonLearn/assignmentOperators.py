x += y # same as x = x + y
print(x)
#subtraction operator
x = 4
x -= 3 #same as x = x - 3
print(x)
#multiplcation operator
x = 4
x *= 3 #same as x = x * 3
print(x)
#division operator
x = 6
x /= 2 #same as x = x / 2
print(x)
#exponentiation operator, **= 
